package class5;

public class E5LogicalOperators {
    public static void main(String[] args) {

        /*
        NOT !
        it flips the boolean values for example if value is true it will change to false
        if false it will change to true
         */
        boolean flag=false;
        // ! operator will reverse the condition, and it will get printed
        System.out.println(!flag);
        System.out.println(flag);

        String name="Semir";
        // only works with boolean data type
        // System.out.println(!name);  will not work

    }
}
