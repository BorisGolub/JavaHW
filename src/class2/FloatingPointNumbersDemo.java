package class2;

public class FloatingPointNumbersDemo {
    public static void main(String[] args) {
        /*by default all floating numbers are double, thats why we
        need to add a "f" "F" at the end of float type variables. no need since we will use double 99% of time*/

        float groceries=20.5F;
        System.out.println(groceries);
        double price=11212.23312;
        System.out.println(price);


    }
}
