package class7;

public class E6WhileLoop {
    public static void main(String[] args) {
        //print these numbers 2 4 6 8 10 12 14 using while loop

        int counter=2;
        while(counter<=14){
            System.out.println(counter);
            counter+=2; // line will increase counter by 2 everytime
        }
    }
}
